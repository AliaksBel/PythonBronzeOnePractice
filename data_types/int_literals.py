# Литералы int для разных систем счисления

int_dec_1 = 1234567
int_dec_2 = 1_234_567
int_dec_3 = -123456

print(int_dec_1)
print(int_dec_2)
print(int_dec_3)

int_bin_1 = 0b1001001
int_bin_2 = 0b100_1001
int_bin_3 = -0b1001001

print(int_bin_1)
print(int_bin_2)
print(int_bin_3)

int_oct_1 = 0o1234567
int_oct_2 = 0o1_234_567
int_oct_3 = -0o1234567

print(int_oct_1)
print(int_oct_2)
print(int_oct_3)

int_hex_1 = 0x123abc
int_hex_2 = 0x12_3a_bc
int_hex_3 = -0x123abc

print(int_hex_1)
print(int_hex_2)
print(int_hex_3)